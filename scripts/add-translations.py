def _re_replace(text: str, eng: str, esp: str) -> str:
    """Reemplazo con limite de palabra (evita solapamientos como 'a car' en 'la carrera')."""
    eng_stripped = eng.strip()
    esp_stripped = esp.strip()
    try:
        pattern = r'(?<!\w)' + re.escape(eng_stripped) + r'(?!\w)'
        return re.sub(pattern, esp_stripped, text, flags=re.IGNORECASE)
    except re.error:
        return text.replace(eng, esp)


def translate(scaffold: str) -> str:
    # 1. Reemplazar placeholders
    text = scaffold.replace("{{ans_1}}", "___").replace("{{ans_2}}", "___")

    is_question = text.strip().endswith("?")
    is_yn  = text.strip().startswith("___") and is_question
    is_neg = re.match(r"^___n", text.strip()) and is_question

    # 2. Yes/No questions: agregar ¿ al inicio
    if is_yn or is_neg:
        text = "¿" + text

    # 3. Frases WH al inicio
    for eng_start, es_start in WH_STARTS.items():
        if text.startswith(eng_start):
            text = es_start + text[len(eng_start):]
            if not text.startswith("¿"):
                text = "¿" + text
            break

    # 4. Frases multi-palabra del diccionario PHRASES (ya ordenadas largo→corto)
    for eng, esp in PHRASES:
        text = _re_replace(text, eng.strip(), esp.strip())

    # 5. Palabras sueltas con límite de palabra
    for eng, esp in EXTRA_WORDS:
        text = _re_replace(text, eng, esp)

    # 6. Inicios de oración con mayúsculas
    for eng, esp in SENTENCE_STARTERS.items():
        if text.startswith(eng):
            text = esp + text[len(eng):]
            break

    # 7. Limpiar espacios dobles
    text = re.sub(r"  +", " ", text).strip()

    # 8. Asegurar cierre de pregunta
    if is_question and "¿" in text and not text.endswith("?"):
        text += "?"
    elif is_question and "¿" not in text:
        text = "¿" + text

    return text


def translate_filled(scaffold: str, correct_answer: str) -> str:
    """
    1. Traduce el scaffold con ___ como placeholders (para detectar WH / yes-no correctamente).
    2. Reemplaza cada ___ con la parte correspondiente de correct_answer.
    """
    translated = translate(scaffold)   # devuelve algo como "¿Dónde ___ tú ayer?"
    parts = [p.strip() for p in correct_answer.split(" / ")]
    for part in parts:
        translated = translated.replace("___", part, 1)
    # Si quedaron ___ sin cubrir (raro), dejarlos como "?"
    translated = translated.replace("___", "?")
    return translated


def main():
    path = "data/questions.json"
    with open(path, "r", encoding="utf-8") as f:
        data = json.load(f)

    total = 0
    for group in data:
        for q in group["questions"]:
            q["scaffold_es"] = translate_filled(q["scaffold"], q["correct_answer"])
            total += 1

    with open(path, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=4)

    print(f"OK: scaffold_es generado para {total} preguntas.")


if __name__ == "__main__":
    main()
